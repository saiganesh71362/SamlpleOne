package com.java.stringmethods;

public class CharAt {

	public static void main(String[] args)
	{
	// charAt method return specific index position value 
		//--> char declare return char element based on index position
		//--> int declare return ASCII alphabets values
      String s1 = "Saiganesh";
      char c = s1.charAt(0);
      int s = s1.charAt(1);
      System.out.println(c); // return specific char values
      System.out.println(s); // return char value ASCII number
	}

}
