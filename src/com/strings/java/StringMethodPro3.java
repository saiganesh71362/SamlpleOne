package com.strings.java;

public class StringMethodPro3 
{  // (toUpperCase,toLowerCase,charAt)

	public static void main(String[] args)
	{ 
       String str1 = "saiganesh";
       String str2 = "SAIGANESH";
       System.out.println(str1.toUpperCase());
       System.out.println(str2.toLowerCase());
       System.out.println(str1.charAt(8)); // charAt method returns index based values return index starts with "0".
	}

}
